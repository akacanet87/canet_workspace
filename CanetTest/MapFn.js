var mapFn = function(map, blockArr ,bgSrc, bgmSrc, blockImg){

	this.img;
	this.blockImg=blockImg;
	this.map=map;
	this.left;
	this.top;
	this.blockArr=blockArr;
	this.map.style.left=-20;
	this.map.style.top=-100;

	this.bgmSrc=bgmSrc;
	this.bgSrc=bgSrc;
	var me = this;

	this.entX;
	this.entY;

	this.init = function(){

		this.audio = new audioCtrl(this.bgmSrc);

		this.audio.init();

		this.map.style.backgroundImage="url('"+this.bgSrc+"')";

		for( var a=0 ; a<20 ; a++ ){

			this.block = new stBlock( this.map, 50, 50, 45*a, 700, this.blockImg);

			this.block.init();

			this.blockArr.push(this.block);

			this.map.appendChild(this.blockArr[a].img);

		}

		this.map.addEventListener("mousemove", function(){

			this.entX=event.clientX;
			this.entY=event.clientY;

			//console.log("x좌표 : "+this.entX+" , y좌표 : "+this.entY);

		});

		this.move();
		
	}

	this.move=function(){

		setTimeout(function(){

			me.move();
		
		}, 10);

	}

}


var stBlock = function( map, width, height, x, y, blockImg ){

	this.map=map;
	this.img;
	this.width=width;
	this.height=height;
	this.x=x;
	this.y=y;
	this.blockImg=blockImg;

	this.init = function(){

		this.img = document.createElement("img");

		this.img.src=this.blockImg;

		this.img.style.width=this.width+"px";
		this.img.style.height=this.height+"px";

		this.img.style.position="absolute";
		this.img.style.left=this.x+"px";
		this.img.style.top=this.y+"px";

		this.map.appendChild(this.img);

	}

}

